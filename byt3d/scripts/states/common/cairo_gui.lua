-- Making this global gives all modules access to it

Gcairo      = require("scripts/cairo_ui/base")

------------------------------------------------------------------------------------------------------------

local Scairo_gui	= NewState()

------------------------------------------------------------------------------------------------------------

function Scairo_gui:Init(w, h)

    self.width = w
    self.height = h

    Gcairo.newObject	= nil
    Gcairo:Init(self.width, self.height)
end

------------------------------------------------------------------------------------------------------------

function Scairo_gui:Begin()

end

------------------------------------------------------------------------------------------------------------

function Scairo_gui:Update(mxi, myi, buttons)
    Gcairo:Update(mxi, myi, buttons)
end

------------------------------------------------------------------------------------------------------------

function Scairo_gui:Render()
    Gcairo:Render()
end

------------------------------------------------------------------------------------------------------------

function Scairo_gui:Finish()
    Gcairo:Finish()
end

------------------------------------------------------------------------------------------------------------

return Scairo_gui

